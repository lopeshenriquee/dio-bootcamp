package aulagetteresetter.escola;

public class Aluno {
	String nome;
	int idade;
	
	//Getter - Pegar
	public String getNome() {
		return nome;
	}
	//Setter - Setar 
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	//Getter - Pegar
	public int getIdade() {
		return idade;
	}
	//Setter - Setar 
	public void setIdade(int idade) {
		this.idade = idade;
	}
	
	
	

}
